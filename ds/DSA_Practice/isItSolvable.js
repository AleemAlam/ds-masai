function runProgram(input) {
  input = input.trim().split("\n");
  const testCase = +input[0].trim();
  let row = 1;
  for (let i = 0; i < testCase; i++) {
    const [p, q, r] = input[row++].trim().split(" ");
    isItSolve(p, q, r);
  }
}

